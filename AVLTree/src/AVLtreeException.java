public class AVLtreeException extends Exception{

	public AVLtreeException()
	{
		
		super();
		
	} //end AVLtreeException	
	
	public AVLtreeException(String message)
	{
		
		super(message);
		
	} //end AVLtreeException
	
	public AVLtreeException(String message, Throwable reason)
	{
		
		super(message, reason);
		
	} //end AVLtreeException
	
	public AVLtreeException(Throwable reason)
	{
		
		super(reason);
		
	} //end AVLtreeException
	
} //end AVLtreeException