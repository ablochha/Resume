import java.awt.*;

/**
 * This class holds static references to all colors used by the application.
 *
 * @author Saquib Mian
 */
public class Colors {

    /**
     * The color of the headers for each sub-panel in {@link com.team23.weather.ScrollableGui}
     */
    public static Color ComponentHeaderColor = Color.white;

    /**
     * The color of the top bar in {@link com.team23.weather.ScrollableGui}
     */
    public static Color HeaderColor = new Color(63, 128, 212);//new Color(55, 90, 136);

    /**
     * The background color of any list items
     */
    public static Color ListItemBackgroundColor = Color.white;

    /**
     * The foreground color of any list items
     */
    public static Color ListItemForegroundColor = Color.black;

    /**
     * The background color of any selected list items
     */
    public static Color SelectedListItemBackgroundColor = Color.blue;

    /**
     * The foreground color of any selected list items
     */
    public static Color SelectedListItemForegroundColor = Color.white;

}
